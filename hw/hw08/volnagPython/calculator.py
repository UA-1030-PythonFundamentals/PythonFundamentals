import math


def ad(n1,n2):
    '''Calculates addition of two numbers'''
    s = n1 + n2
    return s

def prd(n1,n2):
    '''Calculates multiplication of two numbers'''
    m = n1 * n2
    return m

def dvs(n1,n2):
    '''Calculates division of two numbers'''
    f = n1/n2
    return f

def sbt(n1,n2):
    '''Calculates subtration of two numbers'''
    d = n1-n2
    return d

